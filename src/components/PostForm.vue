<template>
	<form @submit.prevent>
		<h3>Создание поста</h3>
		<input
			v-model="post.title"
			class="input"
			type="text"
			placeholder="Название поста"
		/>
		<input
			v-model="post.body"
			class="input"
			type="text"
			placeholder="Описание поста"
		/>

		<my-button class="btn" @click="createPost">Создать</my-button>
	</form>
</template>

<script>
import MyButton from './UI/MyButton.vue';
export default {
	components: { MyButton },
	data() {
		return {
			post: {
				title: '',
				body: '',
			},
		};
	},
	methods: {
		createPost() {
			this.post.id = Date.now();

			this.$emit('create', this.post);

			this.post = {
				title: '',
				body: '',
			};
		},
	},
};
</script>

