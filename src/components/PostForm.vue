<template>
	<form @submit.prevent>
		<h3>Создание поста</h3>
		<input
			v-model="post.title"
			class="input"
			type="text"
			placeholder="Название поста"
		/>
		<input
			v-model="post.body"
			class="input"
			type="text"
			placeholder="Описание поста"
		/>

		<button class="btn" @click="createPost">Создать</button>
	</form>
</template>

<script>
export default {
	data() {
		return {
			post: {
				title: '',
				body: '',
			},
		};
	},
	methods: {
		createPost() {
			this.post.id = Date.now();

			this.$emit('create', this.post);

			this.post = {
				title: '',
				body: '',
			};
		},
	},
};
</script>

